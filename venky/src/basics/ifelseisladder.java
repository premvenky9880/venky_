package basics;

public class ifelseisladder {

	public static void main(String[] args) {
	
       int score=89;
		
		if(score>90)
		{
			System.out.println("grade A");
		}
		else if(score>=80)
		{
			System.out.println("grade B");
		}
		else if(score>=70)
		{
			System.out.println("grade c");
		}
		else if(score>60)
		{
			System.out.println("grade D");
		}
		else
		{
			System.out.println("fail");
		}

	}

}
